package com.naswork.model;

public class SupplierClassify {
    private Integer id;

    private String supplierClassifyId;

    private Integer supplierId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getSupplierClassifyId() {
        return supplierClassifyId;
    }

    public void setSupplierClassifyId(String supplierClassifyId) {
        this.supplierClassifyId = supplierClassifyId;
    }

    public Integer getSupplierId() {
        return supplierId;
    }

    public void setSupplierId(Integer supplierId) {
        this.supplierId = supplierId;
    }
}