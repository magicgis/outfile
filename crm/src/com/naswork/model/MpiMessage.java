package com.naswork.model;

public class MpiMessage {
    private Integer id;

    private String name;

    private String code;

    private String address;

    private String tel;

    private String email;

    private String fax;

    private String aogDeskEmail;

    private String aogHotline;

    private String contact;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFax() {
        return fax;
    }

    public void setFax(String fax) {
        this.fax = fax;
    }

    public String getAogDeskEmail() {
        return aogDeskEmail;
    }

    public void setAogDeskEmail(String aogDeskEmail) {
        this.aogDeskEmail = aogDeskEmail;
    }

    public String getAogHotline() {
        return aogHotline;
    }

    public void setAogHotline(String aogHotline) {
        this.aogHotline = aogHotline;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }
}